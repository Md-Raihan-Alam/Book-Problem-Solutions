CREATE TABLE teaches
(
	id VARCHAR(5),
	course_id VARCHAR(8),
	sec_id VARCHAR(8),
	semester VARCHAR(6),
	year NUMERIC(4,0),
	PRIMARY KEY(ID, course_id, sec_id, semester, year),
